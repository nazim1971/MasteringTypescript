// Basic Data Types

// String type
let myName: string = "Nazim";

// Number type
let roll: number = 123;

// Boolean type
let isNazim: boolean = true;

// Undefined type
let x: undefined = undefined;

// Null type
let y: null = null;

// Assigning a value to a declared variable
let N: number;
N = 36; // Always try to declare and assign values separately when possible for clarity

// Array of strings
let friends: string[] = ['Miko', 'Monika'];

// Array of numbers
let eligibleRollList: number[] = [2, 6];
eligibleRollList.push(12); // Using the `push` method to add values

// Tuple type: fixed-size array with specified types
let coordinates: [number, number] = [1, 7]; // Represents a point in 2D space (x, y)
let ageName: [number, string, boolean] = [50, "Mr. Nazim", true]; // Tuple for age, name, and a boolean flag
